package libj.math;

/**
 * Handy math constants.
 * @author Magnus C. Hyll <magnus@hyll.no>
 */
public class Const {
    
    /**
     * Pi divided by 180. Multiply by this constant when converting from degrees
     * to radians.
     */
    public static final double degToRad = Math.PI / 180.0;
    
    /**
     * 180 divided by pi. Multiply by this constant when converting from radians
     * to degrees.
     */
    public static final double radToDeg = 180.0 / Math.PI;
}
